public class Hello {
    public static void main(String[] args) {
        System.out.println("Hello, world!");
    }
}
// 定义了一个class(类)
// 类名是Hello，public表示类是公开的
// public, class 都是Java的关键字，必须小写；
// Hello是类名，首字母大写；
// { }中是类的定义；
// 类中定义了一个名为main的方法，用()括起来的是方法参数，参数类型是String[]，参数名是args
// public、static用来修饰方法，这里表示它是一个公开的静态方法，void是方法的返回类型